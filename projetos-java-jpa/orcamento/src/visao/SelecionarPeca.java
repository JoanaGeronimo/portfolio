    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        DadosPeca tela = new DadosPeca(null, true);
        tela.setVisible(true);
        if (tela.salvar) {
            Pecas pecas = tela.getPecaAtual();

            pecasdao.adicionar(pecas);

            atualizarTabela();
        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha != -1) {
            Pecas peca = listapecas.get(linha);
            pecasdao.remover(peca);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma linha da tabela.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

