    private void atualizarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();

        modelo.setRowCount(0);

        listapecas.clear();
        listapecas.addAll(pecasdao.getTodosPecas());

        for (Pecas p : listapecas) {
            modelo.addRow(new Object[]{p.getIdpecas(), p.getDescricao(), p.getPreco(), p.getQtdeestoque()});
        }
    }

