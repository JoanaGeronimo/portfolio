    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        DadosPeca tela = new DadosPeca(null, true);
        int linha = tabela.getSelectedRow();
        if (linha != -1) {
            Pecas pecas = listapecas.get(linha);
            tela.setPecaatual(pecas);
            tela.setVisible(true);
            pecas = tela.getPecaAtual();
            pecasdao.alterar(pecas);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma linha da tabela.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Clique em algo por favor!...");
        } else {
            this.pecaSelecionada = listapecas.get(linha);
            this.selecionou = true;
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

