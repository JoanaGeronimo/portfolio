    private void atualizarValorParcelas(double valorTotal) {
        int numParcelas = listaparcelas.size();

        if (numParcelas > 0) {

            double valorParcela = valorTotal / numParcelas;

            for (Parcelas parcela : listaparcelas) {
                parcela.setValor(valorParcela);
            }

            atualizarTabelaParcelas();
        }
    }

    private void btnAddParcelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddParcelaActionPerformed

        double valorTotal = Double.parseDouble(txtValorTotal.getText().replace(",", "."));

        int numParcelas = listaparcelas.size() + 1;

        double valorParcela = valorTotal / numParcelas;

        Date dataParcela = new Date();
        if (numParcelas > 1) {

            Parcelas ultimaParcela = listaparcelas.get(listaparcelas.size() - 1);
            Calendar calendario = Calendar.getInstance();
            calendario.setTime(ultimaParcela.getVencimento());
            calendario.add(Calendar.DAY_OF_MONTH, 30);
            dataParcela = calendario.getTime();
        }

        for (Parcelas parcela : listaparcelas) {
            parcela.setValor(valorParcela);
        }

        Parcelas novaParcela = new Parcelas();
        novaParcela.setValor(valorParcela);
        novaParcela.setVencimento(dataParcela);
        novaParcela.setOrcamento(orcamentoatual);

        listaparcelas.add(novaParcela);

        int contador = 1;
        for (Parcelas parcela : listaparcelas) {
            parcela.setIdparcelas(contador);
            contador++;
        }

        atualizarTabelaParcelas();

    }//GEN-LAST:event_btnAddParcelaActionPerformed

    private void btnRemoverParcelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverParcelaActionPerformed

        if (listaparcelas.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Não há parcelas para remover!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Parcelas parcelaParaAtualizar = listaparcelas.stream()
                .min(Comparator.comparingInt(Parcelas::getIdparcelas))
                .orElse(null);

        if (parcelaParaAtualizar != null) {

            ControleOrcamento controleOrcamento = new ControleOrcamento();
            parcelaParaAtualizar.setDatapagamento(new Date());
            controleOrcamento.alterarParcela(parcelaParaAtualizar);

            listaparcelas.remove(parcelaParaAtualizar);

            int contador = 1;
            for (Parcelas parcela : listaparcelas) {
                parcela.setIdparcelas(contador);
                contador++;
            }

            atualizarTabelaParcelas();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao remover a parcela!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnRemoverParcelaActionPerformed

