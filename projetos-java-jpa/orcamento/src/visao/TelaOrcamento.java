    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        int quantidade = Integer.parseInt(txtQtd.getText());

        ControlePecas pecasdao = new ControlePecas();
        Pecas pecas = pecasdao.obterPecasPorId(pecasSelecionado.getIdpecas());

        if (pecas != null && pecas.getQtdeestoque() >= quantidade) {

            ItemOrcamento itemOrcamento = new ItemOrcamento();
            itemOrcamento.setPecas(pecas);
            itemOrcamento.setQtde(quantidade);
            itemOrcamento.setPreco(Double.parseDouble(txtPreco.getText()));

            int contador = 1;
            listaitens.add(itemOrcamento);

            for (ItemOrcamento I : listaitens) {
                I.setIditem(contador);
                contador += 1;
            }

            pecas.setQtdeestoque(pecas.getQtdeestoque() - quantidade);
            pecasdao.alterar(pecas);

            atualizarTabelaPecas();
            atualizarValorTotal();
        } else {
            JOptionPane.showMessageDialog(this, "Estoque insuficiente para a peça selecionada!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton5ActionPerformed

