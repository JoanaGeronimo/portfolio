    private void atualizarTabelaServico() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaServico.getModel();
        modelo.setRowCount(0);

        for (Servicos s : listaservicos) {
            modelo.addRow(new Object[]{
                s.getIdservico(), s.getServicorealizado(), s.getPreco()});
        }
    }

    private void atualizarTabelaParcelas() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaParcela.getModel();
        modelo.setRowCount(0);

        for (Parcelas p : listaparcelas) {
            modelo.addRow(new Object[]{
                p.getIdparcelas(), data.format(p.getVencimento()), p.getValor()});
        }
    }

    private void atualizarTabelaPecas() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaPeca.getModel();
        modelo.setRowCount(0);

        for (ItemOrcamento i : listaitens) {
            modelo.addRow(new Object[]{
                i.getPecas().getIdpecas(), i.getPecas().getDescricao(), i.getQtde(), i.getPreco()});
        }
    }

    public Orcamento getOrcamentoAtual() {
        orcamentoatual = new Orcamento();
        orcamentoatual.setDataaprovacao(dateAprovacao.getDate());
        orcamentoatual.setDataorcamento(dataOrcamento.getDate());
        orcamentoatual.setDatavalidade(dateValidade.getDate());
        orcamentoatual.setNome(txtNome.getText());
        orcamentoatual.setTelefone(txtTelefone.getText());
        orcamentoatual.setEndereco(txtEndereco.getText());

        double total = Double.parseDouble(txtValorTotal.getText().replace(",", "."));
        orcamentoatual.setValortotal(total);

        for (ItemOrcamento i : listaitens) {
            i.setOrcamento(orcamentoatual);
        }
        for (Servicos s : listaservicos) {
            s.setOrcamento(orcamentoatual);
        }
        for (Parcelas p : listaparcelas) {
            p.setOrcamento(orcamentoatual);
        }

        orcamentoatual.setListaItens(listaitens);
        orcamentoatual.setListaServicos(listaservicos);
        orcamentoatual.setListaParcelas(listaparcelas);

        if (this.codigo != 0) {
            orcamentoatual.setIdorcamento(this.codigo);
        }
        return orcamentoatual;
    }

    public void setOrcamentoAtual(Orcamento orcamentoatual) {
        this.codigo = orcamentoatual.getIdorcamento();
        dataOrcamento.setDate(orcamentoatual.getDataorcamento());
        dateAprovacao.setDate(orcamentoatual.getDataaprovacao());
        dateValidade.setDate(orcamentoatual.getDatavalidade());
        txtEndereco.setText(orcamentoatual.getEndereco());
        txtNome.setText(orcamentoatual.getNome());
        txtTelefone.setText(orcamentoatual.getTelefone());
        txtValorTotal.setText(String.valueOf(orcamentoatual.getValortotal()));

        listaitens.clear();
        listaitens.addAll(orcamentoatual.getListaItens());
        listaparcelas.clear();
        listaparcelas.addAll(orcamentoatual.getListaParcelas());
        listaservicos.clear();
        listaservicos.addAll(orcamentoatual.getListaServicos());

        atualizarTabelaServico();
        atualizarTabelaParcelas();
        atualizarTabelaPecas();

    }

