    private void atualizarValorTotal() {
        double total;
        double total1 = 0.0;
        double total2 = 0.0;

        for (Servicos s : listaservicos) {
            total1 += s.getPreco();
        }
        for (ItemOrcamento o : listaitens) {
            total2 += o.getPreco() * o.getQtde();
        }
        total = total1 + total2;
        txtValorTotal.setText(String.format("%.2f", total));

        atualizarValorParcelas(total);
    }

