    private void atualizarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();

        modelo.setRowCount(0);

        listaorcamento.clear();
        listaorcamento.addAll(orcamentodao.getTodosOrcamento());

        for (Orcamento o : listaorcamento) {
            modelo.addRow(new Object[]{o.getIdorcamento(), data.format(o.getDataorcamento()), o.getValortotal()});
        }
    }
    
    

