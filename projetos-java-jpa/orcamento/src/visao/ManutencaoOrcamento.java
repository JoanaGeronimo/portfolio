    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        TelaOrcamento tela = new TelaOrcamento(null, true);
        tela.setVisible(true);
        if (tela.salvar) {
            Orcamento orcamento = tela.getOrcamentoAtual();

            orcamentodao.adicionar(orcamento);

            atualizarTabela();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        TelaOrcamento tela = new TelaOrcamento(null, true);
        int linha = tabela.getSelectedRow();
        if (linha != -1) {
            Orcamento orcamento = listaorcamento.get(linha);
            tela.setOrcamentoAtual(orcamento);
            tela.setVisible(true);
            orcamento = tela.getOrcamentoAtual();
            orcamentodao.alterar(orcamento);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma linha da tabela.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha != -1) {
            Orcamento orcamento = listaorcamento.get(linha);
            orcamentodao.remover(orcamento);
            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma linha da tabela.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

